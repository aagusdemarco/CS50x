#include <cs50.h>
#include <stdio.h>

int main(void)
{
    int c;
    do
    {
        c = get_int("Change owed: ");
    }
    while (c < 1);

    int q = 25;
    int d = 10;
    int n = 5;
    int p = 1;

    for (int i = 0; i < c; i++)
    {
        if (c >= q)
        {
            c = c - q;
        }
        else if (c >= d)
        {
            c = c - d;
        }
        else if (c >= n)
        {
            c = c - n;
        }
        else if (c >= p)
        {
            c = c - p;
        }
        printf("%i", c);
    }
}
